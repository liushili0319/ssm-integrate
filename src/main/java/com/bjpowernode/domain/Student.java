package com.bjpowernode.domain;

public class Student {

    private Integer id;
    private String name;
    private Integer age;


    public void setId(Integer id) {
        this.id = id;
    }


    public void setName(String name) {
        this.name = name;
    }


    public void setAge(Integer age) {
        this.age = age;
    }
}
